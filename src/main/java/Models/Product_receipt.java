package Models;

import java.sql.Date;

public class Product_receipt {
    String id;
    Date date;
    String productid;
    Integer quantity;
    Integer price;

    public Product_receipt(String id, Date date, String productid, Integer quantity, Integer price) {
        this.id = id;
        this.date = date;
        this.productid = productid;
        this.quantity = quantity;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }

    public String getProductid() {
        return productid;
    }

    public void setProductid(String productid) {
        this.productid = productid;
    }

    public Integer getQuantity() {
        return quantity;
    }

    public void setQuantity(Integer quantity) {
        this.quantity = quantity;
    }

    public Integer getPrice() {
        return price;
    }

    public void setPrice(Integer price) {
        this.price = price;
    }
}
